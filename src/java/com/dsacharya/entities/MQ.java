public class MQ {

    private int qid;
    private int uid;

    public MQ(int qid, int uid) {
        this.qid = qid;
        this.uid = uid;
    }

    public int getQid() {
        return qid;
    }

    public void setQid(int qid) {
        this.qid = qid;
    }

    public int getUid() {
        return uid;
    }

    public void setUid(int uid) {
        this.uid = uid;
    }

}
